// NULL datatype -> Refers to an intentional absence of object values
// for example:
var letters = "abc";
console.log(letters.match(/d/)); // This will return null as the letter d is absent in the variable letters


// UNDEFINED -> This datatype on holds one type of value which is undefined.
// for example:
var sports;
console.log(sports) // This will return undefined as the console.log() function has variable that is unassigned